class AdminRepository extends ServiceEntityRepository implements PasswordUpgraderInterface
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Admin::class);
    }

    /**
     * Used to upgrade (rehash) the user's password automatically over time.
     */
    public function upgradePassword(UserInterface $admin, string $newEncodedPassword): void
    {
        if (!$admin instanceof Admin) {
            throw new UnsupportedadminException(sprintf('Instances of "%s" are not supported.', \get_class($admin)));
        }

        $admin->setPassword($newEncodedPassword);
        $this->_em->persist($admin);
        $this->_em->flush();
    }

